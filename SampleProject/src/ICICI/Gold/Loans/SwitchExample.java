package ICICI.Gold.Loans;

public class SwitchExample {

	public static void main(String[] args) {

		int marks = 75;
		switch (marks) {
		case 100:
			System.out.println("1----Given Number is" + marks);
			break;

		case 75:
			System.out.println("2----Given Number is" + marks);
			break;
		default:
			System.out.println("3----Given Number is" + marks);
			break;

		}
	}
}
